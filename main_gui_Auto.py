import tkinter as tk
from tkinter import ttk

def open_new_window():
    new_window = tk.Tk()
    new_window.title("New Window")
    new_window.geometry("1920x1080")  # Set the geometry to 1920x1080
    new_window.configure(bg="#0C0C18")  # Set the background color to #18152C
    

    








    new_window.mainloop()

# Call the function to open the new window
open_new_window()
